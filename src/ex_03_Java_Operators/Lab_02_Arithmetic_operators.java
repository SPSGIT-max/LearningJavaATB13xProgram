package ex_03_Java_Operators;

public class Lab_02_Arithmetic_operators {
    public static void main(String[] args) {
        int a=200;
        int b=100;
        int c=a+b;
        System.out.println(c);

        c=a-b;
        System.out.println(c);

        c=a*b;
        System.out.println(c);

        c=a/b;
        System.out.println(c);

        c=a%b;
        System.out.println(c);

    }

}
